using System.Collections.Generic;
using System.Text;

namespace ISynergy.Framework.AspNetCore.Blazor.SourceGenerator
{
    /// <summary>
    /// Emits C# source text for the Blazor type registration extension method.
    /// </summary>
    internal static class BlazorRegistrationEmitter
    {
        /// <summary>
        /// Emits a static partial class <c>GeneratedBlazorRegistrations</c> with an
        /// <c>AddBlazorRegistrations(IServiceCollection)</c> extension method that registers
        /// all discovered <c>IView</c>, <c>IWindow</c>, and <c>IViewModel</c> implementors
        /// using their respective DI lifetimes.
        /// </summary>
        /// <param name="types">All discovered Blazor UI types across the compilation.</param>
        /// <param name="rootNamespace">The assembly's root namespace.</param>
        /// <returns>C# source text ready to add to the compilation.</returns>
        public static string Emit(IReadOnlyList<BlazorTypeInfo> types, string rootNamespace)
        {
            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated/>");
            sb.AppendLine("#nullable enable");
            sb.AppendLine();
            sb.AppendLine("[assembly: global::System.Reflection.AssemblyMetadata(\"BlazorRegistrationsGenerated\", \"true\")]");
            sb.AppendLine();
            sb.AppendLine("using Microsoft.Extensions.DependencyInjection;");
            sb.AppendLine("using Microsoft.Extensions.DependencyInjection.Extensions;");
            sb.AppendLine();
            sb.AppendLine($"namespace {rootNamespace};");
            sb.AppendLine();
            sb.AppendLine("[global::System.CodeDom.Compiler.GeneratedCode(\"ISynergy.Framework.AspNetCore.Blazor.SourceGenerator\", \"1.0.0\")]");
            sb.AppendLine("public static partial class GeneratedBlazorRegistrations");
            sb.AppendLine("{");
            sb.AppendLine("    /// <summary>");
            sb.AppendLine("    /// Registers all IView, IWindow, and IViewModel types discovered at compile time.");
            sb.AppendLine("    /// AOT-safe: no reflection or assembly scanning.");
            sb.AppendLine("    /// </summary>");
            sb.AppendLine("    /// <param name=\"services\">The service collection to register types into.</param>");
            sb.AppendLine("    /// <returns>The same <see cref=\"global::Microsoft.Extensions.DependencyInjection.IServiceCollection\"/> for chaining.</returns>");
            sb.AppendLine("    public static global::Microsoft.Extensions.DependencyInjection.IServiceCollection AddBlazorRegistrations(");
            sb.AppendLine("        this global::Microsoft.Extensions.DependencyInjection.IServiceCollection services)");
            sb.AppendLine("    {");

            foreach (var type in types)
            {
                var concrete = type.ConcreteTypeName;
                var abstraction = type.AbstractionTypeName;
                var registerMethod = GetTryAddMethod(type.Lifetime);

                if (!string.IsNullOrEmpty(abstraction))
                {
                    sb.AppendLine($"        services.{registerMethod}<{abstraction}, {concrete}>();");
                }

                sb.AppendLine($"        services.{registerMethod}<{concrete}>();");
            }

            sb.AppendLine("        return services;");
            sb.AppendLine("    }");
            sb.AppendLine("}");

            return sb.ToString();
        }

        private static string GetTryAddMethod(BlazorLifetime lifetime)
        {
            return lifetime switch
            {
                BlazorLifetime.Singleton => "TryAddSingleton",
                BlazorLifetime.Scoped => "TryAddScoped",
                _ => "TryAddTransient",
            };
        }
    }
}
